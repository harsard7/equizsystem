    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        String answer = txtanswers.getText();
        String op1 = txtoption1.getText();
        String op2 = txtoption2.getText();
        String op3 = txtoption3.getText();
      
        if(!(answer.equals(op3)||answer.equals(op2)||answer.equals(op1))){
        JOptionPane.showMessageDialog(this, "Please Enter the Answer Match with Given Options \n Note :Letters Are case sensitive");
        txtanswers.requestFocus();
        }else{
        try {
            QuestionDTO questionDTO=new QuestionDTO();
            SubjectsDTO dto=new SubjectsDTO();
            dto.setSubid(Integer.parseInt(cmbsubjectid.getSelectedItem().toString()));
            dto.setSubjectname(txtsubname.getText());
            questionDTO.setSubjectsDTO(dto);
            questionDTO.setQuestion(txtquestion.getText());
            questionDTO.setOption1(txtoption1.getText());
            questionDTO.setOption2(txtoption2.getText());
            questionDTO.setOption3(txtoption3.getText());
            questionDTO.setAnswers(txtanswers.getText());

            boolean addQuestion = ManageQuestionController.addQuestion(questionDTO);
            if(addQuestion){
                UIManager.put("OptionPane.background", Color.WHITE);
                UIManager.getLookAndFeelDefaults().put("Panel.background", Color.WHITE);
                JOptionPane.showMessageDialog(this, "Question saved");
                loadAllquestion();
                clearAllTextFileds();
            }else{
                JOptionPane.showMessageDialog(this, "Question Not saved");

            }
        } catch (Exception ex) {
            Logger.getLogger(ManageQuestion.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    }//GEN-LAST:event_btn_saveActionPerformed

