private void loadAllsubids(){
    
        try {
            List<SubjectsDTO> allSubjectss = ManageSubjectsController.getAllSubjectss();
            cmbsubjectid.removeAllItems();
            for (SubjectsDTO allSubjects : allSubjectss) {
                int subid = allSubjects.getSubid();
                
                cmbsubjectid.addItem(Integer.toString(subid));
                
            }
        } catch (Exception ex) {
            Logger.getLogger(ManageQuestion.class.getName()).log(Level.SEVERE, null, ex);
        }
}
    private void loadAllquestion(){
        try {
            List<QuestionDTO> allquestion = ManageQuestionController.getAllQuestions();
            DefaultTableModel dtm=(DefaultTableModel) tblQuestion.getModel();
            dtm.setRowCount(0);
            if(allquestion !=null){
                for (QuestionDTO question : allquestion) {
                    Object []rowdata={
                        question.getSubjectsDTO().getSubid(),
                        question.getSubjectsDTO().getSubjectname(),
                        question.getQid(),
                        question.getQuestion(),
                        question.getOption1(),
                        question.getOption2(),
                        question.getOption3(),
                        question.getAnswers()};
                     dtm.addRow(rowdata);
                }
               
            }
            
        } catch (Exception ex) {
            Logger.getLogger(ManageQuestion.class.getName()).log(Level.SEVERE, null, ex);
    }
}
