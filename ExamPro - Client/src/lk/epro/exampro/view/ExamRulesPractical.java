private void loadAllExamids(){

           try {
               List<ExaminationDTO> allExamss = ManageExaminationController.getAllExaminations();
               cmbexamid.removeAllItems();
               for (ExaminationDTO allexams : allExamss) {
                   examdtohashmap.put(allexams.getEid(), allexams);
                   int eid = allexams.getEid();
                   cmbexamid.addItem(Integer.toString(eid));
                   
               }
           } catch (Exception ex) {
               Logger.getLogger(ManageQuestion.class.getName()).log(Level.SEVERE, null, ex);
           }
   }    


