    private void cmbexamidItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbexamidItemStateChanged
        if(cmbexamid.getSelectedIndex()!=-1){
            try {
                ExaminationDTO exam = ManageExaminationController.searchExamination(cmbexamid.getSelectedItem().toString());

                txtsubname.setText(exam.getSubjectsDTO().getSubjectname());
                txtsubid.setText(Integer.toString(exam.getSubjectsDTO().getSubid()));
                txtexamdate.setText(exam.getDate());
            } catch (Exception ex) {
                Logger.getLogger(ManageQuestion.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_cmbexamidItemStateChanged

