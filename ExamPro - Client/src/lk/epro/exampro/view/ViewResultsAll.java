 private void loadAllResult(){
       
        try {
            List<ResultDTO> allresults = ManageResultController.getAllResults();
            DefaultTableModel dtm=(DefaultTableModel) tblresults.getModel();
            dtm.setRowCount(0);
            
            for (ResultDTO result : allresults) {
                
                Object []rowdata={
                    result.getRsid(),
                    result.getExamination().getEid(),
                    result.getExamination().getDate(),
                    result.getRegistration().getRegid(),
                    result.getRegistration().getFname(),
                    result.getExamination().getSubjectsDTO().getSubjectname(),
                    result.getPercentage(),
                    result.getStatus()};
                dtm.addRow(rowdata);
            }
        } catch (Exception ex) {
            Logger.getLogger(ViewResultsAll.class.getName()).log(Level.SEVERE, null, ex);
        }
      }
