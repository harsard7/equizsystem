   private void chartdisplay(){
            String subjectname = examdto.getSubjectsDTO().getSubjectname();
            String fname = regdto.getFname();
         DefaultCategoryDataset dataset=new DefaultCategoryDataset();
         dataset.setValue(30,"Marks", "Pass Marks");
          dataset.setValue(marks,"Marks", "Your Marks");
          JFreeChart chart=ChartFactory.createBarChart("Exam Marks of "+fname,"Subject - "+subjectname, "Marks", dataset,PlotOrientation.VERTICAL,false,true,false);
           CategoryPlot p=chart.getCategoryPlot();
            BarRenderer renderer = (BarRenderer) p.getRenderer();
            Color color =  new Color(26, 79, 225);
           renderer.setSeriesPaint(0, color);
           p.setRangeGridlinePaint(Color.BLUE);
            ChartPanel myChart = new ChartPanel(chart);
           myChart.setVisible(true);
           myChart.setSize(450,350);
            panel.setLayout(new java.awt.BorderLayout());
            panel.add(myChart,BorderLayout.CENTER);
            panel.validate();
    }
   
  



